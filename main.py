def is_palindrome(s):
    # 1. Привести строку к нижнему регистру, чтобы сравнение было нечувствительно к регистру
    s = s.lower()

    # 2. Удалить все символы, кроме букв и цифр (если нужно учитывать только буквы/цифры)
    s = ''.join(char for char in s if char.isalnum())

    # 3. Сравнить строку с её перевёрнутой версией
    return s == s[::-1]


# Примеры использования
print(is_palindrome("А роза упала на лапу Азора"))  # True
print(is_palindrome("Hello, World!"))  # False
